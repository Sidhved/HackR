package src;

import java.util.Scanner;

public class InOut {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int i = sc.nextInt();
        System.out.println(i);
        i = sc.nextInt();
        System.out.println(i);
        i = sc.nextInt();
        System.out.println(i);
        sc.close();
    }
}
